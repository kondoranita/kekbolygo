	<br>
	<p style="text-align:center;" id="zold"><b>Dr. Áder János</b></p>
<br>
	<p style="text-align:justify;"><i>
	Drámai változásoknak vagyunk tanúi és elszenvedői, éljünk a világ bármely tájékán: aszályok és erdőtüzek, viharok és áradások, szennyezett vizek és járványok, pusztuló termőtalaj és savasodó óceánok, rohamosan olvadó jégtakaró és emelkedő tengerszint. Mindez az emberi tevékenység következménye. Ha nem változtatunk, az évszázad végén – bő két emberöltő múlva – már nem lesz visszaút: a Föld jelentős része lakhatatlanná válik.<br>
<br>
	Évtizedek óta mázsás súlyú kérdések nehezednek ránk, és a megválaszolásukhoz szükséges idő vészesen fogy. El kell végre döntenünk, mit kezdünk az egyre gyorsuló klímaváltozással! Mert ma már nem az a kérdés, hogyan mentsük meg a Földet, hanem az: hogyan mentsük meg az emberi civilizációt. Ma még van remény. Hiszen a tudásnak, a technikai megoldásoknak javarészt birtokában vagyunk: csupán eltökéltség és bátorság kell, hogy a jövőnket szolgáló döntéseket meghozzuk. Hogy ne szalasszuk el az utolsó esélyt!<br>
	<br>
	A Kék Bolygó Alapítványt azért hoztuk létre, hogy a klímaváltozás elleni küzdelemben és a környezeti fenntarthatóság érdekében partnerei lehessünk minden, közös jövőnkért tenni akaró, felelős embernek.<br>
	<br>
	Legyen Ön is a szövetségesünk! Szövetségesünk a megelőzésben, a szemléletváltásban, mások meggyőzésében. Önmagunk, gyermekeink, unokáink jövőjéért.</i></p>
